# 1 = path
# 2 = bait
# 3 = big_bait


class Maze:

    def __init__(self):
        self.height = 0
        self.width = 0
        self.bait_count = 0
        self.mtr = []

    def get_avaliable_pos(self, pos):
        moves = [-1, 0], [0, -1], [1, 0], [0, 1]
        avaliables = []
        for move in moves:
            avaliable_move = self.is_avaliable([pos[0] + move[0], pos[1] + move[1]])
            if avaliable_move['avaliable']:
                avaliables.append(avaliable_move['new_pos'])
        return avaliables

    def is_avaliable(self, pos):
        if pos[0] < 0:
            pos[0] = self.width - 1
        if pos[0] >= self.width:
            pos[0] = 0
        if pos[1] < 0:
            pos[1] = self.height - 1
        if pos[1] >= self.height:
            pos[1] = 0
        return {'avaliable': 0 != self.mtr[pos[1]][pos[0]], 'new_pos': pos}

